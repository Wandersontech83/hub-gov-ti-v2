-- ---------------------------------------------------------------------
-- RLS: leitura pública (visualizador) · escrita só autenticada (admin)
-- ---------------------------------------------------------------------
do $rls$
declare t text;
begin
  foreach t in array array[
    'hub_modules_config','hub_incidents','hub_problems','hub_rfcs','hub_cab_meetings',
    'hub_contracts','hub_suppliers','hub_risks','hub_budget_entries','hub_okrs',
    'hub_key_results','hub_evidences','hub_audit_actions','hub_sla_records',
    'hub_vulnerabilities','hub_pam_accounts','hub_ai_models','hub_alerts',
    'hub_activity_log','hub_n8n_config','hub_data_sources']
  loop
    execute format('alter table %I enable row level security', t);
    execute format('drop policy if exists "leitura_publica" on %I', t);
    execute format('create policy "leitura_publica" on %I for select using (true)', t);
    execute format('drop policy if exists "escrita_admin" on %I', t);
    execute format('create policy "escrita_admin" on %I for all to authenticated using (true) with check (true)', t);
  end loop;
end
$rls$;

